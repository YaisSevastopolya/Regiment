                println("безинициативный военнослужащий. Должностные обязанности знает и исполняет не в полном объёме.")
            } else {
                println("зарекомендовал себя с положительной стороны, как подготовленный, дисциплинированный и")
                println("инициативный военнослужащий. Должностные обязанности знает и выполняет в полном объёме.")
            }
            if (infantryUnit.isTrained.toDouble() < 50.0) {
                println("   В профессиональном отношении подготовлен слабо, нуждается в допольнительной профподготовке.")
                println("В ходе проведения учений, полевых выходов, лагерных сборов тяжело приспосабливается к условиям")
                println("обстановки.")
                println("   Физически развит плохо.")
            } else {
                println("   В профессиональном отношении подготовлен хорошо, постоянно совершенствует личную подготовку.")
                println("В ходе проведения учений, полевых выходов, лагерных сборов быстро и легко приспосабливался к")
                println("новым условиям.")
                if (infantryUnit.isSportsman) {
                    print("   Физически развит отлично, имеет серьзные спортивные достижения.")
                } else {
                    println("   Физически развит хорошо, постоянно подтверждает это при сдаче спортивных нормативов.")
                }
            }
            if (infantryUnit.isBattleTried) {
                println("   Нёс службу в условиях боевой обстановки, психологически устойчив и уравновешен")
                if (infantryUnit.combatExperience.toDouble() > 50.0) {
                    println("Имеет широкий боевой опыт, неоднократно принимал непосредственное участие в")
                    println("боевых действиях")
                } else {
                    println("Имеет опыт непосредственного участия в боевых действиях")
                }
            }
            if (infantryUnit.vehicleDriverExperience.toDouble() > 50.0) {
                println("   Хорошо владеет навыками вождения боевых машин, постоянно их совершенствует")
                if (infantryUnit.vehicleCombatDriverExperience.toDouble() > 50.0) {
                    println("Имеет ценнный опыт управления военной техникой в боевой обстановке")
                }
            }
        }
    }
}