                if (infantryUnit.isSportsman) {
                    print("   Физически развит отлично, имеет серьзные спортивные достижения.")
                } else {
                    println("   Физически развит хорошо, постоянно подтверждает это при сдаче спортивных нормативов.")
                }
            }
            if (infantryUnit.isBattleTried) {
                println("   Нёс службу в условиях боевой обстановки, психологически устойчив и уравновешен")
                if (infantryUnit.combatExperience.toDouble() > 50.0) {
                    println("Имеет широкий боевой опыт, неоднократно принимал непосредственное участие в")
                    println("боевых действиях")
                } else {
                    println("Имеет опыт непосредственного участия в боевых действиях")
                }
                if (infantryUnit.commandCombatExperience.toDouble() > 50.0) {
                    println("   Принятие командирских решений в боевых условиях осуществляет оперативно, решительно и")
                    println("непрерывно. Задачи вышестоящего командования выполняются вверенным подразделением точно и в")
                    println("срок.")
                } else {
                    println("   Имеет опыт командования подразделением в бою.")
                }
            }
            if (infantryUnit.vehicleDriverExperience.toDouble() > 50.0) {
                println("   Хорошо владеет навыками вождения боевых машин, постоянно их совершенствует")
                if (infantryUnit.vehicleCombatDriverExperience.toDouble() > 50.0) {
                    println("Имеет ценнный опыт управления военной техникой в боевой обстановке")
                }
            }
        } else {
            println("                                   Служебная характеристика")
            println("   За время службы в войсковой части № N в должности ${(Data.database[infantryUnit.position] ?: error("")).position} ${infantryUnit.fullName} ")
