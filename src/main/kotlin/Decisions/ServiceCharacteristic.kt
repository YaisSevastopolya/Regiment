        if (infantryUnit is CommandUnit) {
            println("                                   Служебная характеристика")
            println("   За время службы в войсковой части № N в должности ${(Data.database[infantryUnit.position] ?: error("")).position} ${infantryUnit.fullName} ")
            if (infantryUnit.isAlcoholic) {
                println("зарекомендовал себя с отрицательной стороны, как не трудолюбивый, не дисциплинированный и")
