                println("зарекомендовал себя с положительной стороны, как трудолюбивый, дисциплинированный и порядочный")
                println("командир. Должностные обязанности знает и выполняет в полном объёме.")
            }
            if (infantryUnit.commandExperience.toDouble() > 40.0) {
                println("   Способен лично выполнять поставленную задачу, а также добиться выполнения поставленных задач")
                println("от подчинённых, проявляя при этом организаторские способности, настойчивость и требовательность.")
